class Theme:
    """
    Defines a "graphically consistent" set of colors and other 
    visual attributes of the views.
    """
    pass
  
 
class Green:
    
    primary = green_700
    variant = green_500
    accent = red_500
    tinted = green_50
    alert = red_900
    background = white
    

class DefaultColor(Green):
    pass
    
    
class DefaultFont:
    font = (
        '-apple-system,'
        'BlinkMacSystemFont,'
        '"Segoe UI",'
        'Roboto,'
        'Helvetica,'
        'Arial,'
        'sans-serif,'
        '"Apple Color Emoji",'
        '"Segoe UI Emoji",'
        '"Segoe UI Symbol"'
    )
    font_m = 14
    
    
class DefaultLook:
    corner_s = 2
    button_border_width = 1
    button_border_style = 'none'
    inner_shadow = 'inset 0 2px 4px 0 rgba(0, 0, 0, 0.06);'


class DefaultTheme(DefaultColor, DefaultFont, DefaultLook):
    pass


def theme(property_name):
    return lambda cls: getattr(cls.current_theme, property_name)

theme.background = theme('background')
theme.font = theme('font')
    
def contrast(property_name):
    return lambda cls: getattr(
        cls.current_theme, property_name
    ).contrast_color()
        
contrast.background = contrast('background')


class Style:
    """
    Defines pseudo style properties for a view, expected to be mostly 
    callable references to the active Theme.
    """
    current_theme = DefaultTheme
        
    
class BaseStyle(Style):
    
    text_alignment = 'center'
    font = theme.font
    font_size = theme('font_m')
    
    
class TextInputStyle(BaseStyle):
    
    text_alignment = 'left'
    background_color = theme.background
    shadow = theme('inner_shadow')
    border_color = theme('primary')
    
    
class ButtonStyle(BaseStyle):
    
    background_color = theme('primary')
    text_color = contrast('primary')
    corner_radius = theme('corner_s')
    border_width = theme('button_border_width')
    border_color = theme('primary')
    border_style = theme('button_border_style')
    

class VariantButtonStyle(ButtonStyle):
    
    background_color = theme('variant')
    text_color = contrast('variant')

